# -*- coding: utf-8 -*-
from selenium.webdriver.firefox.webdriver import WebDriver
from group import Group
from contact import Contact
import unittest

def is_alert_present(wd):
    try:
        wd.switch_to_alert().text
        return True
    except:
        return False


class TestAddGroup(unittest.TestCase):
    def setUp(self):
        self.wd = WebDriver()
        self.wd.implicitly_wait(60)
    
    def test_add_group(self):
        wd = self.wd
        self.open_home_page(wd)
        self.login(wd, username="admin", password="secret")
        self.open_groups_page(wd)
        self.create_group(wd, Group(name="hoho", header="vjl", footer="bho"))
        self.return_to_groups_page(wd)
        self.logout(wd)

    def test_add_empty_group(self):
        wd = self.wd
        self.open_home_page(wd)
        self.login(wd, username="admin", password="secret")
        self.open_groups_page(wd)
        self.create_group(wd, Group(name="", header="", footer=""))
        self.return_to_groups_page(wd)
        self.logout(wd)

    def test_add_contact(self):

        wd = self.wd
        self.open_home_page(wd)
        self.login(wd, username="admin", password="secret")

        self.create_contact(wd, Contact(first_name="asd", middle_name="sdfgh", last_name="hjk", nickname="hoho", title="title", company="comp", address="add", home_tel="+12", mobile_tel="+12", work_tel="+12", fax="+12",
                            email="vhj@llk", email2="vgh@hvj", email3="vjk@vgh", homepage="page", birth_day="3", birth_month="January", birth_year="2001", anniversary_day="3", anniversary_month="May", anniversary_year="1999", address2="add",
                            phone2="phone2", notes="jl"))
        self.logout(wd)

